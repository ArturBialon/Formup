import { HttpClient } from '@angular/common/http';
import { Injectable } from '@angular/core';
import { BehaviorSubject, Observable, ReplaySubject } from 'rxjs';
import { map } from 'rxjs/operators';
import { UserLoginDTO, LoginService } from './autogenerated/autogenerated.service';

@Injectable({
  providedIn: 'root'
})
export class AccountService {
  private currentUserSource = new ReplaySubject<UserLoginDTO>(1);
  public currentUser$: Observable<UserLoginDTO> = this.currentUserSource.asObservable();

  constructor(
    private http: HttpClient,
    private loginService: LoginService
  ) { }

  logout() {
    localStorage.removeItem('user');
    this.currentUserSource.next(null);
  }
}
